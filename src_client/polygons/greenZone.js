let greenZoneId = 0;


gm.events.add("playerEnterPolygon", (polygon) => {
    if (!polygon.isGreenZone)
        return;

    greenZoneId = polygon.id;
    mp.events.callRemote("inGreenZone", greenZoneId);


});

gm.events.add("playerLeavePolygon", (polygon) => {
    if (!polygon.isGreenZone)
        return;

    if (polygon.id !== greenZoneId)
        return;

    mp.events.callRemote("outGreenZone", greenZoneId);
    greenZoneId = 0;
});


let safeZoneTimeId = null;

gm.events.add('safeZone', function (argument) {
    try
    {
        global.isInSafeZone = argument;
        mp.gui.emmit(`window.hudStore.greenZone (${argument})`);
        if(global.isAdmin)
            return;

        if(!argument)
        {
            if(safeZoneTimeId != null)
                clearTimeout(safeZoneTimeId);

            safeZoneTimeId = null;

            global.localplayer.setInvincible(false);
            mp.events.callRemote('IsSafeZone', false);
        }
        else
        {
            if(safeZoneTimeId != null)
                return;

            safeZoneTimeId = setTimeout(function () {
                if(global.isInSafeZone) {
                    global.localplayer.setInvincible(true);
                    mp.events.callRemote('IsSafeZone', true);
                }

                safeZoneTimeId = null;
            },	1000 * 15);
        }
    }
    catch (e)
    {
        mp.events.callRemote("client_trycatch", "player/render", "safeZone", e.toString());
    }
});



global.isGreenZone = (position) => {
    let isZone = false;

    mp.polygons.pool.map((polygon) => {
        if (!isZone && polygon.isGreenZone && mp.polygons.isPositionWithinPolygon(position, polygon, 0))
            isZone = true;
    });

    return isZone;
}

