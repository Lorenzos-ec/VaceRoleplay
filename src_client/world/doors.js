const getDimensionsModel = (model) => {
    const dimensions = mp.game.gameplay.getModelDimensions(model);

    const xAbs = (Math.abs(dimensions.min.x) + Math.abs(dimensions.max.x));
    const yAbs = (Math.abs(dimensions.max.y) - Math.abs(dimensions.min.y));
    const zAbs = (Math.abs(dimensions.min.y) + Math.abs(dimensions.max.y));

    return new mp.Vector3(xAbs, yAbs, zAbs);
}

